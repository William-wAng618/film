package com.william.film.pojo;

public class Class {
    private Integer classLevel;

    private String className;

    private Double classDiscount;

    private Integer classState;

    public Integer getClassLevel() {
        return classLevel;
    }

    public void setClassLevel(Integer classLevel) {
        this.classLevel = classLevel;
    }

    public String getClassName() {
        return className;
    }

    public void setClassName(String className) {
        this.className = className;
    }

    public Double getClassDiscount() {
        return classDiscount;
    }

    public void setClassDiscount(Double classDiscount) {
        this.classDiscount = classDiscount;
    }

    public Integer getClassState() {
        return classState;
    }

    public void setClassState(Integer classState) {
        this.classState = classState;
    }
}